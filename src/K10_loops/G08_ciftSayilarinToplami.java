package K10_loops;

public class G08_ciftSayilarinToplami {

    public static void main(String[] args) {
        //2 basamaklı çift sayıların toplamı

      int sayiToplami=0;

        for (int i = 10; i <99 ; i+=2) {

            sayiToplami+=i;

        }

        System.out.print("İki basamaklı çift sayıların toplamı: "+sayiToplami);

        System.out.println();












    }









}
